#!/usr/bin/env node

import { spawnSync } from "node:child_process";
import {
  cpSync,
  existsSync,
  mkdtempSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

function walkFiles(rootDir) {
  const files = [];
  const stack = [rootDir];
  while (stack.length > 0) {
    const dir = stack.pop();
    if (!dir) continue;
    const entries = readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        stack.push(fullPath);
        continue;
      }
      if (entry.isFile()) files.push(fullPath);
    }
  }
  return files;
}

function rewriteRelativeImportsForNodeNext(filePath) {
  const source = readFileSync(filePath, "utf8");
  const rewritten = source.replace(
    /(\bfrom\s+["'])(\.{1,2}\/[^"']+)(["'])/g,
    (_full, prefix, specifier, suffix) => {
      if (
        specifier.endsWith(".js") ||
        specifier.endsWith(".json") ||
        specifier.endsWith(".ts")
      ) {
        return `${prefix}${specifier}${suffix}`;
      }

      const resolvedPath = path.resolve(path.dirname(filePath), specifier);
      if (existsSync(resolvedPath) && statSync(resolvedPath).isDirectory()) {
        return `${prefix}${specifier}/index.js${suffix}`;
      }

      return `${prefix}${specifier}.js${suffix}`;
    },
  );

  if (rewritten !== source) {
    writeFileSync(filePath, rewritten, "utf8");
  }
}

function renderWrapperIndex(includeExperimental) {
  const modeLabel = includeExperimental
    ? "stable + experimental"
    : "stable";

  return `/**
 * Generated by \`pnpm --filter @bb/core gen:codex-event-types\`.
 *
 * Source command:
 *   codex app-server generate-ts --out <schema-dir>${includeExperimental ? " --experimental" : ""}
 *
 * This file provides a stable, typed wrapper around generated schema files.
 * API surface: ${modeLabel}.
 */

import type { EventMsg } from "./schema/EventMsg.js";
import type { ServerNotification } from "./schema/ServerNotification.js";

export type { EventMsg, ServerNotification };

export type CodexServerNotificationMethod = ServerNotification["method"];
export type CodexServerNotificationForMethod<
  TMethod extends CodexServerNotificationMethod,
> = Extract<ServerNotification, { method: TMethod }>;

export type CodexServerNotificationParamsByMethod = {
  [K in CodexServerNotificationMethod]: CodexServerNotificationForMethod<K>["params"];
};

export type CodexEventMessageType = EventMsg["type"];
export type CodexEventMessageForType<
  TType extends CodexEventMessageType,
> = Extract<EventMsg, { type: TType }>;
`;
}

const scriptPath = fileURLToPath(import.meta.url);
const scriptDir = path.dirname(scriptPath);
const packageRoot = path.resolve(scriptDir, "..");
const generatedRootPath = path.join(
  packageRoot,
  "src",
  "generated",
  "codex-app-server",
);
const schemaOutputPath = path.join(generatedRootPath, "schema");
const wrapperIndexOutputPath = path.join(generatedRootPath, "index.ts");
const legacyOutputPath = path.join(generatedRootPath, "event-names.ts");

const includeExperimental = process.argv.includes("--experimental");
const tempRoot = mkdtempSync(path.join(tmpdir(), "bb-codex-schema-"));
const generatedRoot = path.join(tempRoot, "schema");

try {
  const args = ["app-server", "generate-ts", "--out", generatedRoot];
  if (includeExperimental) {
    args.push("--experimental");
  }

  const result = spawnSync("codex", args, { encoding: "utf8" });
  if (result.status !== 0) {
    throw new Error(
      [
        "Failed to run `codex app-server generate-ts`.",
        result.stderr?.trim(),
      ]
        .filter(Boolean)
        .join("\n"),
    );
  }

  mkdirSync(generatedRootPath, { recursive: true });
  rmSync(schemaOutputPath, { recursive: true, force: true });
  cpSync(generatedRoot, schemaOutputPath, { recursive: true });

  const generatedFiles = walkFiles(schemaOutputPath).filter(
    (filePath) => filePath.endsWith(".ts"),
  );
  for (const filePath of generatedFiles) {
    rewriteRelativeImportsForNodeNext(filePath);
  }

  writeFileSync(
    wrapperIndexOutputPath,
    renderWrapperIndex(includeExperimental),
    "utf8",
  );

  rmSync(legacyOutputPath, { force: true });

  process.stdout.write(
    `Wrote schema to ${schemaOutputPath} and wrapper ${wrapperIndexOutputPath} (${generatedFiles.length} files)\n`,
  );
} finally {
  rmSync(tempRoot, { recursive: true, force: true });
}
